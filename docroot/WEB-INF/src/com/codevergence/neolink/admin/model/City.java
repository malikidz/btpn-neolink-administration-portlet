package com.codevergence.neolink.admin.model;

public class City {
	private int id, propinsiId;
	private String propinsiName, cityName, cityId;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getPropinsiId() {
		return propinsiId;
	}
	public void setPropinsiId(int propinsiId) {
		this.propinsiId = propinsiId;
	}
	public String getPropinsiName() {
		return propinsiName;
	}
	public void setPropinsiName(String propinsiName) {
		this.propinsiName = propinsiName;
	}
	public String getCityName() {
		return cityName;
	}
	public void setCityName(String cityName) {
		this.cityName = cityName;
	}
	public String getCityId() {
		return cityId;
	}
	public void setCityId(String cityId) {
		this.cityId = cityId;
	}
	
	

}
